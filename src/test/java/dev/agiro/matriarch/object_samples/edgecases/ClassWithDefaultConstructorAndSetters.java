package dev.agiro.matriarch.object_samples.edgecases;

public class ClassWithDefaultConstructorAndSetters {
    private String name;
    private int count;

    public ClassWithDefaultConstructorAndSetters() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
